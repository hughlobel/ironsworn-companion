export function getOraclesByCategory(): Map<string, OracleTable[]> {
	const map = new Map<string, OracleTable[]>();
	for (const t of ORACLE_TABLES) {
		if (!map.has(t.category)) map.set(t.category, []);
		map.get(t.category)!.push(t);
	}
	return map;
}

export function getOracleById(id: string): OracleTable | undefined {
	return ORACLE_TABLES.find(t => t.id === id);
}
